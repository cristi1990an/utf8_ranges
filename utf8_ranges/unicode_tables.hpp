struct unicode_range
{
    std::uint32_t first;
    std::uint32_t last;
};

inline constexpr std::tuple<std::size_t, std::size_t, std::size_t> unicode_version{ 17, 0, 0 };

template <std::size_t N>
constexpr bool in_ranges(std::uint32_t scalar, const std::array<unicode_range, N>& ranges) noexcept
{
    std::size_t left = 0;
    std::size_t right = N;
    while (left < right)
    {
        const std::size_t mid = left + (right - left) / 2;
        const unicode_range range = ranges[mid];
        if (scalar < range.first)
        {
            right = mid;
        }
        else if (scalar > range.last)
        {
            left = mid + 1;
        }
        else
        {
            return true;
        }
    }
    return false;
}

